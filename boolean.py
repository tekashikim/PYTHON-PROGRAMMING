is_graduated = False

print("Is student:", is_student)
print("Is graduated:", is_graduated)

# Comparison operators
print("a == b :", a == b)
print("a != b :", a != b)
print("a > b  :", a > b)
print("a < b  :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)